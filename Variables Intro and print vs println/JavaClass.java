package myJavaProject;



public class JavaClass {
	public static void main(String args[]){
		// To make a variable in Java, you need to 1st tell what type of variable you want
		// Example variable types: int, long, float, double, char, boolean
		
		double myDoubleVar;  // declare a variable
		myDoubleVar = 5.28;  // assign a value to the variable 
		
		// This will print 5.28 to the screen
		System.out.print(myDoubleVar); 
	}
}

// Note: There are 2 different print statements: print and println
// print means to print out but to keep cursor on the same line
// println means to print out but move the cursor to a new line afterwards 