package myJavaProject;

// Remember, in Java, everything begins with a class
// If you dont have a class then you dont have Java!

// This is the class
public class JavaClass {
	// This is the method header
	public static void main(String args[]){
		// This is the method body 
		System.out.println("Hello World!");  //Note: println is a built in method, the system.out means to print the line to the system (could also println to a database for example)
	}
}

// Note: The method header along with the method body create the actual "method" 