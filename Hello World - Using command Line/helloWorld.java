class myClass {  // in Java everything begins with a class 
	// inside the class are your methods, i.e. the "things to do" 
	// whenever a java program is run, it needs to have a "main" method 
	public static void main(String args[]){
		// inside the method are the statements/instructions you want your computer to do...
		System.out.println("Hello World!!!");
	}
}

// to compile this file, go in the directory (through command prompt) and enter javac helloWorld.java 
// this will take the java file and turn it into a class file - then you can actually run the class file program 
// Then, type java myClass to see the output of the program 